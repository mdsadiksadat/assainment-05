document.getElementById('card-1').addEventListener('click',function(){
    const product= document.getElementById('title-1');
    const product1= product.innerText;
    const box = document.getElementById('products-name');
    const p = document.createElement('p');
    p.innerText= product1;
    box.appendChild(p);
})
